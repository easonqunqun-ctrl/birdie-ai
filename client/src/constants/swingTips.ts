/**
 * 随机起点（简单 LCG，无需真随机）。
 * 不使用 `Math.random()` 的原因：便于后续想做"按用户 ID 稳定顺序"时改造。
 */
export function pickStartIndex(total = SWING_TIPS.length, seed?: number): number {
  const s = seed ?? Date.now()
  return Math.abs(Math.floor(s / 1000)) % total
}
